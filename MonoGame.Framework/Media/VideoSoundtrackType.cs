        /// This video contains only music.
        /// </summary>
        Music,

        /// <summary>
        /// This video contains only dialog.
        /// </summary>
        Dialog,

        /// <summary>
        /// This video contains music and dialog.
        /// </summary>
        MusicAndDialog,
    }
}
